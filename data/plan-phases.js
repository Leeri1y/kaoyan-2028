// 根据日期返回所属阶段 id（用统一的 [start, end) 区间判断，checkin 和 plan 共用同一逻辑）
function phaseForDate(dateStr) {
  const dt = new Date(dateStr);
  for (const p of PHASES) {
    if (dt >= new Date(p.start) && dt < new Date(p.end)) return p.id;
  }
  return PHASES[PHASES.length - 1].id;
}

// 供 checkin.js 使用：{ [phaseId]: [{id, subject, label}] }
function buildPhaseTasks() {
  const map = {};
  PHASES.forEach(p => {
    map[p.id] = p.tasks.map(t => ({ id: t.id, subject: t.s, label: t.l }));
  });
  return map;
}

module.exports = { PHASES, phaseForDate, buildPhaseTasks };
